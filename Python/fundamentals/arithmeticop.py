#arithmetic operators (+,-,*,/,%,//,**)

num1 = 100

num2 = 3


add_num = num1+num2

print(f"{num1}+{num2}={add_num}")


diff_num = num1-num2

print(f"{num1}-{num2}={diff_num}")


product_num = num1*num2

print(f"{num1}*{num2}={product_num}")


div_num = num1/num2

print(f"{num1}/{num2}={div_num}")


mod_num = num1%num2

print(f"{num1}%{num2}={mod_num}")


expo_num = num1**num2

print(f"{num1}**{num2}={expo_num}")


floor_num = num1//num2

print(f"{num1}//{num2}={floor_num}")